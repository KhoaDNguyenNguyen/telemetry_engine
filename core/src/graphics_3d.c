void Graphics3D_ComputeRotationMatrix(float *matrix, float angle_x, float angle_y, float angle_z) {
    if (!matrix) return;

    const float cx = cosf(angle_x);
    const float sx = sinf(angle_x);
    const float cy = cosf(angle_y);
    const float sy = sinf(angle_y);
    const float cz = cosf(angle_z);
    const float sz = sinf(angle_z);

    matrix[0] = cy * cz;
    matrix[1] = -cy * sz;
    matrix[2] = sy;

    matrix[3] = cx * sz + cz * sx * sy;
    matrix[4] = cx * cz - sx * sy * sz;
    matrix[5] = -cy * sx;

    matrix[6] = sx * sz - cx * cz * sy;
    matrix[7] = cz * sx + cx * sy * sz;
    matrix[8] = cx * cy;
}

void Graphics3D_RotateVertices(const Vector3D *restrict input, Vector3D *restrict output, 
                               const float *restrict matrix, size_t count) {
    if (!input || !output || !matrix || count == 0) return;

#if defined(__aarch64__)
    __asm__ volatile (
        "ld1 {v0.4s, v1.4s, v2.4s}, [%[mat]] \n\t"
        "1: \n\t"
        "ld1 {v3.4s}, [%[in]], #16 \n\t"
        "fmul v4.4s, v0.4s, v3.s[0] \n\t"
        "fmla v4.4s, v1.4s, v3.s[1] \n\t"
        "fmla v4.4s, v2.4s, v3.s[2] \n\t"
        "st1 {v4.4s}, [%[out]], #16 \n\t"
        "subs %[cnt], %[cnt], #1 \n\t"
        "b.ne 1b \n\t"
        : [in] "+r" (input), [out] "+r" (output), [cnt] "+r" (count)
        : [mat] "r" (matrix)
        : "v0", "v1", "v2", "v3", "v4", "memory", "cc"
    );
#else
    #pragma GCC ivdep
    for (size_t i = 0; i < count; ++i) {
        const float x = input[i].x;
        const float y = input[i].y;
        const float z = input[i].z;

        output[i].x = x * matrix[0] + y * matrix[1] + z * matrix[2];
        output[i].y = x * matrix[3] + y * matrix[4] + z * matrix[5];
        output[i].z = x * matrix[6] + y * matrix[7] + z * matrix[8];
        output[i].w = 1.0f;
    }
#endif
}

void Graphics3D_ProjectVertices(const Vector3D *restrict input, Point2D *restrict output, 
                                size_t count, uint16_t screen_w, uint16_t screen_h, 
                                float fov, float viewer_distance) {
    if (!input || !output || count == 0) return;

    const float center_x = (float)screen_w * 0.5f;
    const float center_y = (float)screen_h * 0.5f;

    for (size_t i = 0; i < count; ++i) {
        const float z_offset = input[i].z + viewer_distance;
        const float z_factor = (z_offset <= 0.1f) ? 0.1f : z_offset; 
        const float inv_z = fov / z_factor;

        output[i].x = (int16_t)(input[i].x * inv_z + center_x);
        output[i].y = (int16_t)(input[i].y * inv_z + center_y);
    }
}

void Graphics3D_DrawLine(uint16_t *restrict fb, uint16_t fb_width, uint16_t fb_height, 
                         int16_t x0, int16_t y0, int16_t x1, int16_t y1, uint16_t color) {
    if (!fb) return;

    const int32_t dx = abs((int32_t)x1 - (int32_t)x0);
    const int32_t sx = (x0 < x1) ? 1 : -1;
    const int32_t dy = -abs((int32_t)y1 - (int32_t)y0);
    const int32_t sy = (y0 < y1) ? 1 : -1;
    
    int32_t err = dx + dy;
    int32_t e2;

    int32_t current_x = (int32_t)x0;
    int32_t current_y = (int32_t)y0;
    const int32_t limit_w = (int32_t)fb_width;
    const int32_t limit_h = (int32_t)fb_height;

    while (1) {
        if (current_x >= 0 && current_x < limit_w && current_y >= 0 && current_y < limit_h) {
            fb[(uint32_t)current_y * fb_width + (uint32_t)current_x] = color;
        }

        if (current_x == (int32_t)x1 && current_y == (int32_t)y1) break;
        
        e2 = 2 * err;
        
        if (e2 >= dy) { 
            err += dy; 
            current_x += sx; 
        }
        if (e2 <= dx) { 
            err += dx; 
            current_y += sy; 
        }
    }
}

