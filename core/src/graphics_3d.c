void Graphics3D_DrawChar(uint16_t *restrict fb, uint16_t fb_width, uint16_t fb_height, 
                         int16_t x, int16_t y, char c, uint16_t color, uint8_t scale) {
    uint8_t char_code = (uint8_t)c;
    
    /* Discard non-printable ASCII definitions */
    if (!fb || char_code < 32 || char_code >= 127) return;

    const uint8_t *glyph = font8x8_basic[char_code - 32];

    for (int32_t i = 0; i < 8; ++i) {
        uint8_t row = glyph[i];
        for (int32_t j = 0; j < 8; ++j) {
            if (row & (1 << (7 - j))) {
                for (int32_t dy = 0; dy < scale; ++dy) {
                    for (int32_t dx = 0; dx < scale; ++dx) {
                        int32_t px = (int32_t)x + (j * scale) + dx;
                        int32_t py = (int32_t)y + (i * scale) + dy;
                        if (px >= 0 && px < (int32_t)fb_width && py >= 0 && py < (int32_t)fb_height) {
                            fb[(uint32_t)py * fb_width + (uint32_t)px] = color;
                        }
                    }
                }
            }
        }
    }
}

void Graphics3D_DrawString(uint16_t *restrict fb, uint16_t fb_width, uint16_t fb_height, 
                           int16_t x, int16_t y, const char *str, uint16_t color, uint8_t scale) {
    if (!fb || !str) return;
    
    /* Upcast to 32-bit integer to avert truncation during multiplication accumulation */
    int32_t cursor_x = (int32_t)x;
    
    while (*str) {
        Graphics3D_DrawChar(fb, fb_width, fb_height, (int16_t)cursor_x, y, *str, color, scale);
        cursor_x += (int32_t)(8 * scale);
        str++;
    }
}
